const LOG_PREFIX = "[export.print]";

export function escapeHtml(value: unknown): string {
  const str = String(value ?? "");
  return str
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;")
    .replace(/'/g, "&#39;");
}

export function maskDobForPublic(
  dob?: string | null,
  mask: boolean = PUBLIC_DOB_MASKING
): string {
  if (!dob) return "--";
  if (!mask) return dob;

  const [year, month] = dob.split("-");
  if (!year) return "--";

  const safeMonth = month && month !== "00" ? month : "--";
  return `${year}-${safeMonth}`;
}

type TournamentMeta = Pick<
  Database["public"]["Tables"]["tournaments"]["Row"],
  "title" | "city" | "start_date" | "end_date"
>;

type PlayerRow = Pick<
  Database["public"]["Tables"]["players"]["Row"],
  "rank" | "name" | "rating" | "dob" | "gender" | "state" | "city" | "club"
> & {
  federation?: string | null;
};

export type WinnersExportRow = {
  category: string;
  prizePlace: number | string | null;
  playerName: string;
  playerRank: number | string | null;
  amount: number | string | null;
  trophy: boolean | null;
  medal: boolean | null;
  typeLabel?: string | null;
  groupLabel?: string | null;
};

function formatDateRange(meta: Partial<TournamentMeta>): string {
  const range = [meta.start_date, meta.end_date].filter(Boolean).join(" – ");
  return range;
}

function formatGeneratedAt(date: Date): string {
  return date.toLocaleString("en-IN", {
    year: "numeric",
    month: "short",
    day: "2-digit",
    hour: "2-digit",
    minute: "2-digit"
  });
}

function buildTableRows(rows: PlayerRow[], maskDob: boolean): string {
  return rows
    .map(row => {
      const cells: Array<string | number | null | undefined> = [
        row.rank,
        row.name,
        row.rating,
        maskDobForPublic(row.dob, maskDob),
        row.gender,
        row.state || "—",
        row.city || "—",
        row.club || "—",
        row.federation || "—"
      ];

      return `<tr>${cells
        .map(value => `<td>${escapeHtml(value)}</td>`)
        .join("")}</tr>`;
    })
    .join("");
}

