export function openPrintWindow(html: string, filenameBase = "players-export"): boolean {
  const printWindow = window.open("", "_blank");
  if (!printWindow) {
    return false;
  }

  printWindow.document.open();
  printWindow.document.write(html);
  printWindow.document.close();
  printWindow.document.title = filenameBase;

  setTimeout(() => {
    try {
      printWindow.focus();
      printWindow.print();
    } catch (error) {
      console.error(`${LOG_PREFIX} print failed`, error);
    }
  }, 75);

  return true;
}

export async function exportPlayersViaPrint({
  tournamentId,
  maskDob = PUBLIC_DOB_MASKING
}: {
  tournamentId: string;
  maskDob?: boolean;
}) {
  const start = performance.now();
  console.log(`${LOG_PREFIX} start tournament=${tournamentId} version=players-summary`);

  try {
    const { data: tournament, error: tournamentError } = await supabase
      .from("tournaments")
      .select("title, city, start_date, end_date")
      .eq("id", tournamentId)
      .maybeSingle();

    if (tournamentError) {
      throw tournamentError;
    }

    const { data: players, count, usedColumns } = await safeSelectPlayersByTournament(
      tournamentId,
      ['rank', 'name', 'rating', 'dob', 'gender', 'state', 'city', 'club', 'federation'],
      { column: 'rank', ascending: true }
    );

    if (!players || players.length === 0) {
      throw new Error('No players found for this tournament');
    }

    console.log('[print] Loaded players for print', { count, usedColumns });

    const html = buildPlayersPrintHtml(tournament ?? null, players as PlayerRow[], { maskDob });
    const opened = openPrintWindow(html, `players-${tournamentId}`);

    if (!opened) {
      throw new Error("Popup blocked. Allow popups to print or save as PDF.");
    }

    const duration = Math.round(performance.now() - start);
    console.log(`${LOG_PREFIX} ok tournament=${tournamentId} duration_ms=${duration}`);
  } catch (error: unknown) {
    const duration = Math.round(performance.now() - start);
    const message = error instanceof Error ? error.message : String(error);
    console.error(`${LOG_PREFIX} error tournament=${tournamentId} message=${message}`);
    console.log(`${LOG_PREFIX} error duration_ms=${duration}`);
    throw error;
  }
}
