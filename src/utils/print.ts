function buildTableHtml<T>(rows: T[], columns: ExportColumn<T>[]): string {
  const visibleColumns = filterEmptyColumns(rows, columns);
  const headerRow = visibleColumns.map(col => `<th>${escapeHtml(col.label)}</th>`).join("");
  const bodyRows = rows
    .map(row => {
      const cells = visibleColumns
        .map(col => `<td>${escapeHtml(formatExportValue(col.value(row)))}</td>`)
        .join("");
      return `<tr>${cells}</tr>`;
    })
    .join("");

  return `<table>
    <thead>
      <tr>${headerRow}</tr>
    </thead>
    <tbody>
      ${bodyRows}
    </tbody>
  </table>`;
}

